/*********************************************************** 
函数名称：LCD_write_char
函数功能：显示英文字符
入口参数：c	:  显示的字符
出口参数：无 
备 注： 
***********************************************************/
void LCD_write_char(unsigned char c)
{
    unsigned char i; 
	 c-=0x20;                //ASCII码减去 0x20

    for (i=0; i<6; i++)
        LCD_write_byte(ASCII_6_8[c][i], 1);
}
/*-------------------------------------------
;名称: 英文字符串显示函数
;参数：*s：英文字符串指针
---------------------------------------------*/
void LCD_write_String(unsigned char X,unsigned char Y,unsigned char *s)
{
	LCD_set_XY(X,Y);
	while (*s) 	                 //等效*s!='\0'
	{
		LCD_write_char(*s);
		s++;
	}
} 
