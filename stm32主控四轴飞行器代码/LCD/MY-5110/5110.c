void LCD_write_byte(unsigned char dat, unsigned char command)
  {
    unsigned char i;
     LCD_SCE = 0;				  //5110片选有效，允许输入数据
    if (command == 0)			  //写命令
          LCD_DC = 0;				  
    else  LCD_DC = 1;			  //写数据
	for(i=0;i<8;i++)			  //传送8bit数据
		{ 
 	if(dat&0x80)		            
				SDIN = 1;
			else
				SDIN = 0;
			LCD_CLK = 0;
			dat = dat << 1;
			LCD_CLK = 1;          //时钟上升沿 写
	}
     LCD_SCE = 1;				   //禁止5110
  }


/*********************************************************** 
函数名称：LCD_init
函数功能：5110初始化
入口参数：无
出口参数：无 
备 注： 
***********************************************************/
void LCD5110_init(void)
  {
    LCD_SCE = 0;       // 片选LCD
	LCD_REST = 0;      // 产生一个让LCD复位的低电平脉冲
		delay_us(10);//10us最合适
    LCD_REST = 1;

    LCD_write_byte(0x21, 0);	// LCD模式设置:芯片活动，水平寻址，使用扩展指令
    LCD_write_byte(0xc4, 0);	// 设置液晶偏置电压
    LCD_write_byte(0x06, 0);	// 温度校正
    LCD_write_byte(0x13, 0);	// 1:48
    LCD_write_byte(0x20, 0);	// 使用基本命令，V=0，水平寻址
    LCD_clear();	           // 清屏
    LCD_write_byte(0x0c, 0);	// 设定显示模式，正常显示
   
    LCD_SCE = 1;      //禁止LCD
  }

/*********************************************************** 
函数名称：LCD_set_XY
函数功能：设置LCD坐标函数
入口参数：X       ：0－83  横坐标
          Y       ：0－5   纵坐标 
出口参数：无 
备 注： 
***********************************************************/
void LCD_set_XY(unsigned char X, unsigned char Y)
  {
      LCD_write_byte(0x80 | X, 0);        // X     行(横坐标)
	  LCD_write_byte(0x40 | Y, 0);		  // column	 列(纵坐标)
  }

/*------------------------------------------
LCD_clear: LCD清屏函数 
--------------------------------------------*/
void LCD_clear(void)
{
	unsigned char t;
	unsigned char k;
	LCD_set_XY(0,0);
	for(t=0;t<6;t++)
	{ 
		for(k=0;k<84;k++)
		{ 
			LCD_write_byte(0x00,1);
				
		} 
	}
}


